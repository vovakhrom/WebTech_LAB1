package by.bsuir.wt.lab1.main;

public class Ball {
    public final double weight;
    public final String color;
    public Ball (double weight,String color){
        this.weight=weight;
        this.color=color;
    }

    public double getWeight() {
        return weight;
    }
    public String getColor() {
        return color;
    }

}
